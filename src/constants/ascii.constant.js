/**
 * See https://www.asciitable.com/
 */

/**
 * Character for "null"
 */
export const NUL = '\0';

/**
 * Character for "carriage return"
 */
export const CR = '\r';

/**
 * Character for "line feed"
 */
export const LF = '\n';

/**
 * Character for "Windows' new line"
 */
export const CRLF = CR + LF;

/**
 * Character for "semicolon"
 */
export const SC = ';';

/**
 * ASCII code for character "null"
 */
export const NUL_BYTE = NUL.charCodeAt(0);

/**
 * ASCII code for character "carriage return"
 */
export const CR_BYTE = CR.charAt(0);

/**
 * ASCII code for character "line feed"
 */
export const LF_BYTE = LF.charCodeAt(0);

/**
 * ASCII code for character "semicolon"
 */
export const SC_BYTE = SC.charCodeAt(0);
